#!/usr/bin/env python3
"""
Trajectory follower for smooth waypoint following from CSV trajectory files.

Reads trajectory CSV files generated by generate_synchronized_trajectories.py and
follows them smoothly using a lookahead-based controller (pure pursuit style).

Usage:
  ros2 run testbed_bringup trajectory_follower \
    --ros-args \
    -p trajectory_csv:=/path/to/trajectory.csv \
    -p vrpn_pose_topic:=/vrpn_mocap/target/pose \
    -p cmd_vel_topic:=/target/cmd_vel
"""

from __future__ import annotations

import csv
import math
import threading
import time
from pathlib import Path
from typing import Optional, Tuple

import rclpy
from geometry_msgs.msg import Point, PoseStamped, TwistStamped
from rclpy.callback_groups import ReentrantCallbackGroup
from rclpy.node import Node
from rclpy.qos import (
    DurabilityPolicy,
    HistoryPolicy,
    QoSProfile,
    ReliabilityPolicy,
)
from builtin_interfaces.msg import Time as TimeMsg
from visualization_msgs.msg import Marker, MarkerArray

from mua_nbv_py_utils.transforms import yaw_from_quat, quat_from_yaw


class TrajectoryFollower(Node):
    """
    Smooth trajectory follower using lookahead-based control.
    
    Reads a CSV trajectory file and follows it smoothly using a pure pursuit-style
    controller with lookahead distance for smooth cornering.
    """

    def __init__(self):
        super().__init__("trajectory_follower")
        self.cb = ReentrantCallbackGroup()

        # ---- Parameters ----
        self.declare_parameter("trajectory_csv", "")
        self.declare_parameter("vrpn_pose_topic", "/vrpn_mocap/target/pose")
        self.declare_parameter("cmd_vel_topic", "/target/cmd_vel")
        self.declare_parameter("cmd_frame_id", "base_link")
        self.declare_parameter("cmd_stamp_zero", True)

        # Control parameters
        self.declare_parameter("control_rate_hz", 30.0)
        self.declare_parameter("max_control_rate_hz", 60.0)
        self.declare_parameter("min_loop_sleep_sec", 0.005)
        self.declare_parameter("max_v", 0.15)
        self.declare_parameter("max_w", 0.6)
        self.declare_parameter("k_heading", 1.0)
        self.declare_parameter("k_dist", 0.8)
        self.declare_parameter("min_v", 0.02)
        self.declare_parameter("stop_distance_m", 0.05)
        self.declare_parameter("v_turn_penalty", 0.5)
        self.declare_parameter("heading_slow_rad", 1.2)
        self.declare_parameter("heading_slow_scale", 0.4)

        # Lookahead for smooth following
        self.declare_parameter("lookahead_distance_m", 0.3)
        self.declare_parameter("lookahead_min_m", 0.1)
        self.declare_parameter("lookahead_max_m", 0.5)
        self.declare_parameter("lookahead_speed_factor", 0.5)  # lookahead scales with speed

        # Trajectory following
        self.declare_parameter("trajectory_tolerance_m", 0.1)  # How close to final point
        self.declare_parameter("use_time_based", True)  # If True, follow trajectory timestamps (ensures sync)
        self.declare_parameter("time_scale", 1.0)  # Speed multiplier for time-based following
        self.declare_parameter("sync_mode", "time_based")  # "time_based" or "lookahead" or "hybrid"
        # Hybrid mode: use time-based for waypoint sync, lookahead for smoothness between waypoints

        # Safety
        self.declare_parameter("enable_cmd", True)
        self.declare_parameter("dry_run", False)
        self.declare_parameter("log_status", True)
        self.declare_parameter("log_period_sec", 1.0)

        # Waypoint visualization
        self.declare_parameter("publish_waypoints_markers", True)
        self.declare_parameter("waypoints_markers_topic", "/experiment/trajectory/waypoints")
        self.declare_parameter("waypoints_frame_id", "world")
        self.declare_parameter("waypoints_line_width", 0.03)
        self.declare_parameter("waypoints_point_scale", 0.08)
        self.declare_parameter("waypoints_alpha", 0.9)

        # ---- Read parameters ----
        traj_csv = str(self.get_parameter("trajectory_csv").value)
        if not traj_csv:
            raise ValueError("trajectory_csv parameter must be set")
        self.trajectory_csv = Path(traj_csv)
        if not self.trajectory_csv.is_file():
            raise FileNotFoundError(f"Trajectory CSV not found: {self.trajectory_csv}")

        self.vrpn_pose_topic = str(self.get_parameter("vrpn_pose_topic").value)
        self.cmd_vel_topic = str(self.get_parameter("cmd_vel_topic").value)
        self.cmd_frame_id = str(self.get_parameter("cmd_frame_id").value)
        self.cmd_stamp_zero = bool(self.get_parameter("cmd_stamp_zero").value)

        self.control_rate_hz = float(self.get_parameter("control_rate_hz").value)
        self.max_control_rate_hz = float(self.get_parameter("max_control_rate_hz").value)
        self.min_loop_sleep_sec = float(self.get_parameter("min_loop_sleep_sec").value)
        self.max_v = float(self.get_parameter("max_v").value)
        self.max_w = float(self.get_parameter("max_w").value)
        self.k_heading = float(self.get_parameter("k_heading").value)
        self.k_dist = float(self.get_parameter("k_dist").value)
        self.min_v = float(self.get_parameter("min_v").value)
        self.stop_distance_m = float(self.get_parameter("stop_distance_m").value)
        self.v_turn_penalty = float(self.get_parameter("v_turn_penalty").value)
        self.heading_slow_rad = float(self.get_parameter("heading_slow_rad").value)
        self.heading_slow_scale = float(self.get_parameter("heading_slow_scale").value)

        self.lookahead_distance_m = float(self.get_parameter("lookahead_distance_m").value)
        self.lookahead_min_m = float(self.get_parameter("lookahead_min_m").value)
        self.lookahead_max_m = float(self.get_parameter("lookahead_max_m").value)
        self.lookahead_speed_factor = float(self.get_parameter("lookahead_speed_factor").value)

        self.trajectory_tolerance_m = float(self.get_parameter("trajectory_tolerance_m").value)
        self.use_time_based = bool(self.get_parameter("use_time_based").value)
        self.time_scale = float(self.get_parameter("time_scale").value)
        self.sync_mode = str(self.get_parameter("sync_mode").value)

        self.enable_cmd = bool(self.get_parameter("enable_cmd").value)
        self.dry_run = bool(self.get_parameter("dry_run").value)
        self.log_status = bool(self.get_parameter("log_status").value)
        self.log_period_sec = float(self.get_parameter("log_period_sec").value)

        self.publish_waypoints_markers = bool(self.get_parameter("publish_waypoints_markers").value)
        self.waypoints_markers_topic = str(self.get_parameter("waypoints_markers_topic").value)
        self.waypoints_frame_id = str(self.get_parameter("waypoints_frame_id").value)
        self.waypoints_line_width = float(self.get_parameter("waypoints_line_width").value)
        self.waypoints_point_scale = float(self.get_parameter("waypoints_point_scale").value)
        self.waypoints_alpha = float(self.get_parameter("waypoints_alpha").value)

        # ---- Load trajectory ----
        self.trajectory = self._load_trajectory()
        self.get_logger().info(f"Loaded {len(self.trajectory)} trajectory points from {self.trajectory_csv}")

        # ---- State ----
        self._mtx = threading.Lock()
        self.current_pose: Optional[PoseStamped] = None
        self.trajectory_idx = 0
        self.trajectory_start_time: Optional[float] = None
        self._t_log_prev = 0.0
        self._active = False

        # ---- ROS I/O ----
        qos_profile_sensor_data = QoSProfile(
            history=HistoryPolicy.KEEP_LAST,
            depth=1,
            reliability=ReliabilityPolicy.BEST_EFFORT,
        )
        self.sub_pose = self.create_subscription(
            PoseStamped, self.vrpn_pose_topic, self._on_pose, qos_profile_sensor_data, callback_group=self.cb
        )
        self.pub_cmd = self.create_publisher(TwistStamped, self.cmd_vel_topic, 10)

        # Waypoint markers publisher (latched for RViz)
        if self.publish_waypoints_markers:
            qos_markers = QoSProfile(
                history=HistoryPolicy.KEEP_LAST,
                depth=1,
                reliability=ReliabilityPolicy.RELIABLE,
                durability=DurabilityPolicy.TRANSIENT_LOCAL,
            )
            self.pub_waypoints = self.create_publisher(MarkerArray, self.waypoints_markers_topic, qos_markers)

        # Control timer
        dt_control = 1.0 / min(self.control_rate_hz, self.max_control_rate_hz)
        self.create_timer(dt_control, self._control_tick, callback_group=self.cb)

        self.get_logger().info("----------------------------------------------------------------")
        self.get_logger().info(f"📁 Trajectory: {self.trajectory_csv} ({len(self.trajectory)} points)")
        self.get_logger().info(f"📡 Pose: {self.vrpn_pose_topic}")
        self.get_logger().info(f"🕹️  cmd_vel: {self.cmd_vel_topic}")
        self.get_logger().info(f"🔧 Control: max_v={self.max_v} max_w={self.max_w} lookahead={self.lookahead_distance_m}m")
        self.get_logger().info(f"🧯 Safety: enable_cmd={self.enable_cmd} dry_run={self.dry_run}")
        if self.publish_waypoints_markers:
            self.get_logger().info(f"📊 Waypoints markers: {self.waypoints_markers_topic} (frame: {self.waypoints_frame_id})")
        self.get_logger().info("----------------------------------------------------------------")

        # Publish waypoint markers on startup
        if self.publish_waypoints_markers:
            self._publish_waypoints_markers()

    def _load_trajectory(self) -> list[Tuple[float, float, float, float]]:
        """Load trajectory from CSV: (time_s, x, y, z, yaw)."""
        traj = []
        with open(self.trajectory_csv, 'r') as f:
            reader = csv.DictReader(f)
            for row in reader:
                t = float(row['time_s'])
                x = float(row['x'])
                y = float(row['y'])
                z = float(row['z'])
                yaw = float(row.get('yaw', 0.0))
                traj.append((t, x, y, z, yaw))
        return traj

    def _on_pose(self, msg: PoseStamped):
        with self._mtx:
            self.current_pose = msg
            if self.trajectory_start_time is None:
                self.trajectory_start_time = time.time()
                self._active = True
                self.get_logger().info("Started trajectory following")

    def _find_time_based_goal(self, elapsed_time: float) -> Optional[Tuple[float, float, float, int]]:
        """
        Find goal point based on elapsed time since trajectory start.
        Returns (x, y, yaw, trajectory_idx) or None if complete.
        """
        if len(self.trajectory) == 0:
            return None
        
        # Scale time by time_scale
        scaled_time = elapsed_time * self.time_scale
        
        # Find the trajectory segment containing this time
        for i in range(len(self.trajectory) - 1):
            t0, x0, y0, z0, yaw0 = self.trajectory[i]
            t1, x1, y1, z1, yaw1 = self.trajectory[i + 1]
            
            if t0 <= scaled_time <= t1:
                # Interpolate
                if t1 > t0:
                    alpha = (scaled_time - t0) / (t1 - t0)
                else:
                    alpha = 0.0
                
                x = x0 + alpha * (x1 - x0)
                y = y0 + alpha * (y1 - y0)
                yaw = y0 + alpha * (yaw1 - yaw0)
                
                return (x, y, yaw, i)
        
        # If past the end, return last point
        if scaled_time >= self.trajectory[-1][0]:
            _, tx, ty, _, tyaw = self.trajectory[-1]
            return (tx, ty, tyaw, len(self.trajectory) - 1)
        
        return None

    def _find_lookahead_point(self, x: float, y: float, yaw: float, lookahead: float) -> Optional[Tuple[float, float, float]]:
        """
        Find the lookahead point on the trajectory using pure pursuit algorithm.
        
        Returns (x, y, yaw) of the lookahead point, or None if trajectory is complete.
        """
        if self.trajectory_idx >= len(self.trajectory):
            return None

        # Start searching from current trajectory index
        best_idx = self.trajectory_idx
        best_dist = float('inf')
        
        # Find closest point on trajectory ahead of current position
        for i in range(self.trajectory_idx, len(self.trajectory)):
            _, tx, ty, _, _ = self.trajectory[i]
            dx = tx - x
            dy = ty - y
            dist = math.hypot(dx, dy)
            
            # Check if this point is ahead (in direction of travel)
            angle_to_point = math.atan2(dy, dx)
            angle_diff = abs(math.atan2(math.sin(angle_to_point - yaw), math.cos(angle_to_point - yaw)))
            
            # Prefer points ahead (within 90 degrees)
            if angle_diff < math.pi / 2 and dist < best_dist:
                best_dist = dist
                best_idx = i
        
        # Update trajectory index
        self.trajectory_idx = best_idx
        
        # Find point at lookahead distance
        for i in range(best_idx, len(self.trajectory)):
            _, tx, ty, _, tyaw = self.trajectory[i]
            dx = tx - x
            dy = ty - y
            dist = math.hypot(dx, dy)
            
            if dist >= lookahead:
                # Interpolate if needed for exact lookahead distance
                if i > best_idx:
                    _, tx_prev, ty_prev, _, tyaw_prev = self.trajectory[i-1]
                    dx_prev = tx_prev - x
                    dy_prev = ty_prev - y
                    dist_prev = math.hypot(dx_prev, dy_prev)
                    
                    if dist_prev < lookahead < dist:
                        # Interpolate
                        alpha = (lookahead - dist_prev) / (dist - dist_prev)
                        tx_interp = tx_prev + alpha * (tx - tx_prev)
                        ty_interp = ty_prev + alpha * (ty - ty_prev)
                        tyaw_interp = tyaw_prev + alpha * (tyaw - tyaw_prev)
                        return (tx_interp, ty_interp, tyaw_interp)
                
                return (tx, ty, tyaw)
        
        # If we reach the end, return the last point
        if len(self.trajectory) > 0:
            _, tx, ty, _, tyaw = self.trajectory[-1]
            return (tx, ty, tyaw)
        
        return None

    def _compute_control(self, x: float, y: float, yaw: float, goal_x: float, goal_y: float, goal_yaw: float) -> Tuple[float, float]:
        """
        Compute control command (v, w) using heading-based controller.
        Same as target_stepper controller for consistency.
        """
        dx = goal_x - x
        dy = goal_y - y
        dist = math.hypot(dx, dy)

        if dist < self.stop_distance_m:
            return 0.0, 0.0

        target_heading = math.atan2(dy, dx)
        heading_error = math.atan2(math.sin(target_heading - yaw), math.cos(target_heading - yaw))

        w = self.k_heading * heading_error
        w = max(-self.max_w, min(self.max_w, w))

        v = min(self.max_v, self.k_dist * dist)
        v = max(0.0, v)

        # Reduce speed while turning
        v = v / (1.0 + self.v_turn_penalty * abs(w))

        # Slow down for large heading errors
        if abs(heading_error) > self.heading_slow_rad:
            v *= self.heading_slow_scale

        v = max(self.min_v, v) if v > 0.0 else 0.0

        return v, w

    def _control_tick(self):
        if not self._active or not self.enable_cmd:
            return

        with self._mtx:
            if self.current_pose is None or self.trajectory_start_time is None:
                return

            p = self.current_pose.pose.position
            x, y = float(p.x), float(p.y)
            o = self.current_pose.pose.orientation
            yaw = yaw_from_quat((o.x, o.y, o.z, o.w))

            # Calculate elapsed time since trajectory start
            elapsed_time = time.time() - self.trajectory_start_time

            # Choose goal based on sync mode
            if self.use_time_based or self.sync_mode == "time_based":
                # Time-based following: ensures synchronization at waypoints
                goal_result = self._find_time_based_goal(elapsed_time)
                if goal_result is None:
                    self._stop()
                    if self.log_status:
                        t_now = time.time()
                        if t_now - self._t_log_prev >= self.log_period_sec:
                            self.get_logger().info("✅ Trajectory complete (time-based)")
                            self._t_log_prev = t_now
                    return
                
                goal_x, goal_y, goal_yaw, traj_idx = goal_result
                self.trajectory_idx = traj_idx
                
                # For time-based, we can add a small lookahead for smoothness
                if self.sync_mode == "hybrid":
                    # Hybrid: time-based position with lookahead for smooth steering
                    current_speed = self.max_v * 0.5
                    lookahead = self.lookahead_distance_m * 0.5  # Smaller lookahead in hybrid
                    lookahead = max(self.lookahead_min_m, min(self.lookahead_max_m, lookahead))
                    
                    # Find a point slightly ahead on the trajectory for smoother steering
                    lookahead_result = self._find_time_based_goal(elapsed_time + lookahead / self.max_v)
                    if lookahead_result is not None:
                        goal_x, goal_y, goal_yaw, _ = lookahead_result
                
            else:
                # Lookahead-based following: smooth but may not maintain exact sync
                if self.trajectory_idx >= len(self.trajectory):
                    self._stop()
                    if self.log_status:
                        t_now = time.time()
                        if t_now - self._t_log_prev >= self.log_period_sec:
                            self.get_logger().info("✅ Trajectory complete (lookahead-based)")
                            self._t_log_prev = t_now
                    return

                # Get current speed for adaptive lookahead
                current_speed = self.max_v * 0.5  # Estimate
                lookahead = self.lookahead_distance_m + self.lookahead_speed_factor * current_speed
                lookahead = max(self.lookahead_min_m, min(self.lookahead_max_m, lookahead))

                # Find lookahead point
                goal = self._find_lookahead_point(x, y, yaw, lookahead)
                if goal is None:
                    self._stop()
                    return

                goal_x, goal_y, goal_yaw = goal

            # Check if reached final point
            dist_to_final = math.hypot(x - self.trajectory[-1][1], y - self.trajectory[-1][2])
            if dist_to_final < self.trajectory_tolerance_m:
                self._stop()
                if self.log_status:
                    t_now = time.time()
                    if t_now - self._t_log_prev >= self.log_period_sec:
                        self.get_logger().info(f"✅ Reached final waypoint (dist={dist_to_final:.3f}m)")
                        self._t_log_prev = t_now
                return

            # Compute control
            v, w = self._compute_control(x, y, yaw, goal_x, goal_y, goal_yaw)

            # Publish command
            cmd = TwistStamped()
            cmd.header.stamp = TimeMsg() if self.cmd_stamp_zero else self.get_clock().now().to_msg()
            cmd.header.frame_id = self.cmd_frame_id
            cmd.twist.linear.x = float(v)
            cmd.twist.angular.z = float(w)

            if not self.dry_run:
                self.pub_cmd.publish(cmd)

            # Log status
            if self.log_status:
                t_now = time.time()
                if t_now - self._t_log_prev >= self.log_period_sec:
                    progress = (self.trajectory_idx / len(self.trajectory)) * 100 if len(self.trajectory) > 0 else 0
                    mode_str = "time-based" if (self.use_time_based or self.sync_mode == "time_based") else "lookahead"
                    self.get_logger().info(
                        f"Following ({mode_str}): idx={self.trajectory_idx}/{len(self.trajectory)} ({progress:.1f}%) "
                        f"elapsed={elapsed_time:.1f}s v={v:.3f} w={w:.3f}"
                    )
                    self._t_log_prev = t_now

    def _publish_waypoints_markers(self):
        if not self.publish_waypoints_markers or len(self.trajectory) == 0:
            return

        stamp = self.get_clock().now().to_msg()

        # Create line strip marker
        mk_line = Marker()
        mk_line.header.stamp = stamp
        mk_line.header.frame_id = self.waypoints_frame_id
        mk_line.ns = "trajectory_waypoints"
        mk_line.id = 0
        mk_line.type = Marker.LINE_STRIP
        mk_line.action = Marker.ADD
        mk_line.scale.x = float(self.waypoints_line_width)
        mk_line.color.r = 0.1
        mk_line.color.g = 0.9
        mk_line.color.b = 0.2
        mk_line.color.a = float(self.waypoints_alpha)
        mk_line.pose.orientation.w = 1.0

        # Create points marker
        mk_pts = Marker()
        mk_pts.header.stamp = stamp
        mk_pts.header.frame_id = self.waypoints_frame_id
        mk_pts.ns = "trajectory_waypoints"
        mk_pts.id = 1
        mk_pts.type = Marker.POINTS
        mk_pts.action = Marker.ADD
        mk_pts.scale.x = float(self.waypoints_point_scale)
        mk_pts.scale.y = float(self.waypoints_point_scale)
        mk_pts.color.r = 0.1
        mk_pts.color.g = 0.6
        mk_pts.color.b = 1.0
        mk_pts.color.a = float(self.waypoints_alpha)
        mk_pts.pose.orientation.w = 1.0

        # Add trajectory points
        for _, x, y, z, _ in self.trajectory:
            p = Point()
            p.x = float(x)
            p.y = float(y)
            p.z = float(z)
            mk_line.points.append(p)
            mk_pts.points.append(p)

        # Publish MarkerArray
        arr = MarkerArray()
        arr.markers = [mk_line, mk_pts]
        self.pub_waypoints.publish(arr)
        self.get_logger().info(f"Published {len(self.trajectory)} waypoints to {self.waypoints_markers_topic}")

    def _stop(self):
        try:
            cmd = TwistStamped()
            cmd.header.stamp = TimeMsg() if self.cmd_stamp_zero else self.get_clock().now().to_msg()
            cmd.header.frame_id = self.cmd_frame_id
            cmd.twist.linear.x = 0.0
            cmd.twist.angular.z = 0.0
            if self.enable_cmd and not self.dry_run:
                self.pub_cmd.publish(cmd)
        except Exception:
            # Ignore errors during shutdown (publisher context may be invalid)
            pass


def main(args=None):
    rclpy.init(args=args)
    node = TrajectoryFollower()
    try:
        rclpy.spin(node)
    except KeyboardInterrupt:
        pass
    finally:
        node._stop()
        node.destroy_node()
        rclpy.shutdown()


if __name__ == "__main__":
    main()
